            <h1>ABOUT US</h1>
            <p>  Instrumentation engineering is the engineering specialization focused on the principle and operation of measuring instruments that are used in design and configuration of automated systems in electrical, pneumatic hydraulic etc. domains. In an industrial undertaking of any kind Instrumentation Engineers are considered to be the eyes and ears for maintaining and suggesting improvements to existing instruments that are critical to the project. Instrumentation Engineers design, construct, and maintain instruments and instrumentation systems that are necessary to assure better quality and efficiency of the product for the end user. They typically work for industries with automated processes, such as chemical or manufacturing plants, with the goal of improving system productivity, reliability, safety, optimization, and stability.
</p>
            <P>The Instrumentation department is one of oldest engineering branch of this institute established in June 1996. The department has experienced and dedicated faculty members to cater to the academic activities. This department aims to mould all the students with strong foundation and technological skills in area and strives to provide value-based education to meet the country's demand for quality professionals. The department has full–fledged laboratories to meet practical requirements of students. 

            </P>
